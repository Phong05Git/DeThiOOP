    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        dshh.clear();
        model.setRowCount(0);
        try (BufferedReader br = new BufferedReader(new FileReader("hanghoa.txt"))) {
            String line;
            int lineCount = 0;
            while ((line = br.readLine()) != null) {
                lineCount++;
                if (line.trim().isEmpty()) {
                    continue;
                }

                String p[] = line.split("\\$");
                valid(p.length < 6, "Dòng " + lineCount
                        + " trong file hanghoa.txt thiếu dữ liệu!");

                boolean loai = p[0].trim().equals("DT");
                String ma = p[1].trim();
                for(HangHoa hh : dshh){
                    valid(hh.getMa().equalsIgnoreCase(ma), "Mã trùng! Sửa lại mã ở dòng " + lineCount
                    + " trong file hanghoa.txt!");
                }
                String ten = p[2].trim();
                double giaN = Double.parseDouble(p[3].trim());
                double giaB = Double.parseDouble(p[4].trim());

                HangHoa hh;

                if (loai) {
                    double giaS = Double.parseDouble(p[5].trim());
                    hh = new DienThoai(giaS, ma, ten, giaN, giaB);

                } else {
                    double kt = Double.parseDouble(p[5].trim());
                    hh = new MayTinhBang(kt, ma, ten, giaN, giaB);
                }
                dshh.add(hh);
            }
            loadTable();
            JOptionPane.showMessageDialog(this, "Lấy dữ liệu thành công!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            dshh.clear();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (dshh.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Danh sách trống! Bấm nút \"LẤY DỮ LIỆU\" trước!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            String ma = txtTim.getText().trim();

            valid(ma.isEmpty(), "Vui lòng nhập mã hàng cần tìm!");

            boolean found = false;
            for (HangHoa hh : dshh) {
                if (ma.equalsIgnoreCase(hh.getMa())) {
                    found = true;
                    JOptionPane.showMessageDialog(this, hh.toString());
                    break;
                }
            }
            valid(!found, "Không tìm thấy mã " + ma);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (dshh.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Danh sách trống! Bấm nút \"LẤY DỮ LIỆU\" trước!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        for (int i = 0; i < dshh.size() - 1; i++) {
            for (int j = 0; j < dshh.size() - i - 1; j++) {
                if (dshh.get(j).loiNhuan() < dshh.get(j + 1).loiNhuan()) {
                    HangHoa temp = dshh.get(j);
                    dshh.set(j, dshh.get(j + 1));
                    dshh.set(j + 1, temp);
                }
            }
        }
        loadTable();
        JOptionPane.showMessageDialog(this, "Đã sắp xếp!");
    }//GEN-LAST:event_jButton3ActionPerformed

