    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        dssp.clear();
        try (BufferedReader br = new BufferedReader(new FileReader("sanpham.txt"))) {
            String line;
            int lineCount = 0;
            while ((line = br.readLine()) != null) {
                lineCount++;
                if (line.trim().isEmpty()) {
                    continue;
                }

                String p[] = line.split(",");
                if (p.length < 6) {
                    throw new Exception("Dòng " + lineCount + " thiếu thông tin:" + line);
                }

                String loai = p[0].trim();
                String ma = p[1].trim();
                String ten = p[2].trim();
                double gia = Double.parseDouble(p[5].trim());;
                if (loai.equals("CHÍNH HÃNG")) {
                    String nxs = p[3].trim();
                    int bh = Integer.parseInt(p[4].trim());;
                    dssp.add(new SanPhamChinhHang(nxs, bh, ma, ten, gia));
                } else {
                    String nguonGoc = p[3].trim();
                    String status = p[4].trim();
                    dssp.add(new SanPhamXachTay(nguonGoc, status, ma, ten, gia));
                }
            }
            JOptionPane.showMessageDialog(this, "Lấy dữ liệu thành công!");
        } catch (Exception e) {
            showE("Lỗi đọc file:" + e.getMessage());
            dssp.clear();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void JBUTTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBUTTONActionPerformed
        // TODO add your handling code here:
        if (dssp.isEmpty()) {
            showE("Danh sách rỗng!");
            return;
        }

        String ma = txtTim.getText().trim();
        if (ma.isEmpty()) {
            showE("Vui lòng nhập mã sản phẩm!");
            return;
        }

        boolean found = false;

        for (SanPham sp : dssp) {
            if (sp.getMaSP().equalsIgnoreCase(ma)) {
                found = true;
                taSP.setText(sp.toString());
                break;
            }
        }

        if (!found) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy mã hàng: " + ma);
        }
    }//GEN-LAST:event_JBUTTONActionPerformed

