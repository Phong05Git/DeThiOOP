    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (txtMa.getText().trim().isEmpty()
                || txtTen.getText().trim().isEmpty()
                || txtGia.getText().trim().isEmpty()
                || txt1.getText().trim().isEmpty()
                || txt2.getText().trim().isEmpty()) {
            showE("Vui lòng nhập đủ thông tin!");
            return;
        }

        try {
            String ma = txtMa.getText();
            String ten = txtTen.getText();
            String loai = cbLoai.getSelectedItem().toString();
            double gia = Double.parseDouble(txtGia.getText());
            
            for(SanPham sp : dssp){
                if(ma.equals(sp.getMaSP())){
                    showE("Mã sản phẩm đã tồn tại!");
                    return;
                }
            }
            
            if (loai.equals("CHÍNH HÃNG")) {
                String nxs = txt1.getText();
                int bh = Integer.parseInt(txt2.getText());
                dssp.add(new SanPhamChinhHang(nxs, bh, ma, ten, gia));
            } else {
                String nguonGoc = txt1.getText();
                String status = txt2.getText();
                dssp.add(new SanPhamXachTay(nguonGoc, status, ma, ten, gia));
            }
            
            JOptionPane.showMessageDialog(this, "Đã thêm vào danh sách!");
            txtMa.setText("");
            txtMa.requestFocus();
            txtTen.setText("");
            txtGia.setText("");
            txt1.setText("");
            txt2.setText("");
        } catch (NumberFormatException e) {
            showE("Giá bán/Bảo hành phải là số!");
        } catch (Exception e) {
            showE("Lỗi:" + e.getMessage());
        }

    }//GEN-LAST:event_jButton3ActionPerformed

