    private void loadTable() {
        model.setRowCount(0);

        for (HangHoa hh : dshh) {
            if (hh instanceof DienThoai) {
                DienThoai dt = (DienThoai) hh;
                model.addRow(new Object[]{
                    dt.getMa(), dt.getTen(),
                    String.format("%,.2f", dt.getGiaN()),
                    String.format("%,.2f", dt.getGiaB()),
                    "Điện thoại",
                    String.format("%,.2f", dt.getGiaSim()),
                    null,
                    String.format("%,.2f", dt.loiNhuan())
                });
            } else if (hh instanceof MayTinhBang) {
                MayTinhBang mtb = (MayTinhBang) hh;
                model.addRow(new Object[]{
                    mtb.getMa(), mtb.getTen(),
                    String.format("%,.2f", mtb.getGiaN()),
                    String.format("%,.2f", mtb.getGiaB()),
                    "Máy tính bảng",
                    null,
                    mtb.getKtMan(),
                    String.format("%,.2f", mtb.loiNhuan())
                });
            }
        }
    }

